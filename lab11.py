def quicksort(arr):
    # Базовый случай: если массив пуст или содержит один элемент, возвращаем его
    if len(arr) <= 1:
        return arr
    
    # Выбираем опорный элемент (например, последний элемент массива)
    pivot = arr[-1]
    
    # Разделяем массив на две части: элементы меньше и больше опорного элемента
    left = [x for x in arr[:-1] if x <= pivot]  # элементы меньше или равные опорному
    right = [x for x in arr[:-1] if x > pivot]  # элементы больше опорного
    
    # Рекурсивно сортируем левую и правую части и объединяем с опорным элементом
    return quicksort(left) + [pivot] + quicksort(right)

# Пример использования
arr = [13, 11, 12, 6, 5, 7]
print("Отсортированный список:", quicksort(arr))

